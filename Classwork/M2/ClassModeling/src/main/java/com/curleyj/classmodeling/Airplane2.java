package com.curleyj.classmodeling;

/**
 *
 * @author Jake
 */
public class Airplane2 {
    private String typeOfPlane;
    private double maxSpeedOfPlane;
    private String directionOfTravel;
    private double rateOfAltitudeChange;
    private double windSpeed;
    
    public Airplane2() {
        
    }

    public String getTypeOfPlane() {
        return typeOfPlane;
    }

    public void setTypeOfPlane(String typeOfPlane) {
        this.typeOfPlane = typeOfPlane;
    }

    public double getMaxSpeedOfPlane() {
        return maxSpeedOfPlane;
    }

    public void setMaxSpeedOfPlane(double maxSpeedOfPlane) {
        this.maxSpeedOfPlane = maxSpeedOfPlane;
    }

    public String getDirectionOfTravel() {
        return directionOfTravel;
    }

    public void setDirectionOfTravel(String directionOfTravel) {
        this.directionOfTravel = directionOfTravel;
    }

    public double getRateOfAltitudeChange() {
        return rateOfAltitudeChange;
    }

    public void setRateOfAltitudeChange(double rateOfAltitudeChange) {
        this.rateOfAltitudeChange = rateOfAltitudeChange;
    }

    public double getWindSpeed() {
        return windSpeed;
    }

    public void setWindSpeed(double windSpeed) {
        this.windSpeed = windSpeed;
    }
    
    
}
