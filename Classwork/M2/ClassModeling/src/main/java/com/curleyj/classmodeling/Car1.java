package com.curleyj.classmodeling;

/**
 *
 * @author Jake
 */
public class Car1 {
    private String color;
    private String modelMake;
    private int modelYear;
    private double price;
    private double currentMilage;
    private double milesPerGallon;
    private int engineHorsepower;
    
    public Car1() {
        
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getModelMake() {
        return modelMake;
    }

    public void setModelMake(String modelMake) {
        this.modelMake = modelMake;
    }

    public int getModelYear() {
        return modelYear;
    }

    public void setModelYear(int modelYear) {
        this.modelYear = modelYear;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public double getCurrentMilage() {
        return currentMilage;
    }

    public void setCurrentMilage(double currentMilage) {
        this.currentMilage = currentMilage;
    }

    public double getMilesPerGallon() {
        return milesPerGallon;
    }

    public void setMilesPerGallon(double milesPerGallon) {
        this.milesPerGallon = milesPerGallon;
    }

    public int getEngineHorsepower() {
        return engineHorsepower;
    }

    public void setEngineHorsepower(int engineHorsepower) {
        this.engineHorsepower = engineHorsepower;
    }
    
    
}
