package com.curleyj.classmodeling;

/**
 *
 * @author Jake
 */
public class Car2 {
    private String color;
    private String modelMake;
    private double maxSpeed;
    private double rateOfAcceleration;
    private double weight;
    
    public Car2() {
        
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getModelMake() {
        return modelMake;
    }

    public void setModelMake(String modelMake) {
        this.modelMake = modelMake;
    }

    public double getMaxSpeed() {
        return maxSpeed;
    }

    public void setMaxSpeed(double maxSpeed) {
        this.maxSpeed = maxSpeed;
    }

    public double getRateOfAcceleration() {
        return rateOfAcceleration;
    }

    public void setRateOfAcceleration(double rateOfAcceleration) {
        this.rateOfAcceleration = rateOfAcceleration;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }
    
    
}
