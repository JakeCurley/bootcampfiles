package com.curleyj.flooringmastery.dto;

/**
 *
 * @author Jake
 */
public class counter {
    String name;
    int count;

    public counter (String name) {
        this.count = count;
    }
    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
    
    public String getName() {
        return name;
    }
    
    public void setName(String name) {
        this.name = name;
    }
    
    
}
